function checkPageForColor(color) {

    // lowercase for majority of reading
    color = color.toLowerCase()
        // trying to decrease number of words that include colors
    var spacedColor = ` ${color}`

    if (color == "yellow") {
        color = 'gold'
    }


    // get main text by paragraph tag
    var allParagraphs = document.getElementsByTagName("p")

    for (para of allParagraphs) {
        // if the desired color exists, edit the element
        if (para.innerText.includes(spacedColor)) {
            console.log(para)

            // this element is contains at least one instance of the word
            var paraString = para.innerText

            var revisedParaString = paraString.replace(spacedColor, `<span style="color:${color};">${spacedColor}</span>`);

            para.innerHTML = revisedParaString
        }
    }
}

